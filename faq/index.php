                <h5><a href="#question-17" class="h_question" id="question-17">チェックイン・チェックアウトは何時ですか？</a></h5>
                <p>チェックインは午後４時、チェックアウトは午前10時です。</p>

                <h5><a href="#question-18" class="h_question" id="question-18">お部屋に食器や調理器具はありますか？</a></h5>
                <p>食器は定員数分を揃えております。調理器具やカトラリー、炊飯器などもひと通り揃っておりますので、室内でのお食事をどうぞお楽しみください。</p>

