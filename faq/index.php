                <h5><a href="#question-27" class="h_question" id="question-27">ヒルトン･ハワイアン･ビレッジ・ワイキキ・ビーチリゾートの敷地内で毎週金曜日に花火が上がると聞きましたが、本当ですか？</a></h5>
